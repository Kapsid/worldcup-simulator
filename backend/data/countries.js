export const getTopRankedTeams = (count = 32, excludeHost = null) => {
  let topTeams = countries
    .filter(country => !excludeHost || country.code !== excludeHost)
    .sort((a, b) => a.fifaRanking - b.fifaRanking)
    .slice(0, count)
  
  return topTeams
}

